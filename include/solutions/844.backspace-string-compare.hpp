// @lc code=start
class Solution {
public:
    bool backspaceCompare(std::string S, std::string T) {
        return build(S) == build(T);
    }

    std::string build(std::string S) {
        std::string s1{};
        for (auto ch: S) {
            if (ch == '#') {
                if (!s1.empty()) s1.pop_back();
            } else {
                s1.push_back(ch);
            }
        }
        return move(s1);
    }
};
